CREATE TABLE smashapp.users (
  USERID CHAR(36) NOT NULL COMMENT '',
  USERNAME VARCHAR(32) NOT NULL COMMENT '',
  EMAIL VARCHAR(256) NOT NULL COMMENT '',
  PASSWORDHASH CHAR(128) NOT NULL CHARACTER SET 'cp1251' COMMENT '',
  PRIMARY KEY (USERID)  COMMENT '',
  UNIQUE INDEX USERID_UNIQUE (USERID ASC)  COMMENT '',
  UNIQUE INDEX USERNAME_UNIQUE (USERNAME ASC)  COMMENT '');
CREATE TABLE smashapp.entrants (
  UUID CHAR(36) NOT NULL COMMENT '',
  TOURNAMENT_UUID CHAR(36) NOT NULL COMMENT '',
  USER_UUID CHAR(36) NULL COMMENT '',
  TAG VARCHAR(255) NOT NULL COMMENT '',
  SEED INT NULL DEFAULT -1 COMMENT '',
  PRIMARY KEY (UUID)  COMMENT '',
  UNIQUE INDEX UUID_UNIQUE (UUID ASC)  COMMENT '');
CREATE TABLE smashapp.matches (
  UUID CHAR(36) NOT NULL COMMENT '',
  NEXTMATCHUUIDWINNER CHAR(36) NULL COMMENT '',
  NEXTMATCHUUIDLOSER CHAR(36) NULL COMMENT '',
  FIRSTPREVIOUSMATCH CHAR(36) NULL COMMENT '',
  SECONDPREVIOUSMATCH CHAR(36) NULL COMMENT '',
  WINSENTRANTONE INT NULL DEFAULT 0 COMMENT '',
  WINSENTRANTTWO INT NULL DEFAULT 0 COMMENT '',
  ROUNDNUMBER INT DEFAULT 0 COMMENT '',
  ENTRANTONE CHAR(36) NULL COMMENT '',
  ENTRANTTWO CHAR(36) NULL COMMENT '',
  WINNER CHAR(36) NULL COMMENT '',
  TOURNAMENTUUID CHAR(36) NOT NULL COMMENT '',
  PRIMARY KEY (UUID)  COMMENT '',
  UNIQUE INDEX UUID_UNIQUE (UUID ASC)  COMMENT '');
CREATE TABLE smashapp.tournaments (
  UUID CHAR(36) NOT NULL COMMENT '',
  NAME VARCHAR(200) NOT NULL COMMENT '',
  START_TIME DATETIME(6) NULL COMMENT '',
  SIZE_LIMIT INT NULL COMMENT '',
  LOCATION VARCHAR(500) NULL COMMENT '',
  GAME VARCHAR(200) NOT NULL COMMENT '',
  HOST CHAR(36) NULL COMMENT '',
  FORMAT VARCHAR(50) NOT NULL COMMENT '',
  DESCRIPTION VARCHAR(500) NULL COMMENT '',
  IS_BRACKET_VISIBLE INT NULL DEFAULT 0 COMMENT '',
  PRIMARY KEY (UUID)  COMMENT '',
  UNIQUE INDEX UUID_UNIQUE (UUID ASC)  COMMENT '',
  UNIQUE INDEX NAME_UNIQUE (NAME ASC)  COMMENT '');